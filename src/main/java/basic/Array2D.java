package basic;

public class Array2D {

	public static void ajay() {
		int[][] scores = new int[][] { { 4, 5 }, { 56, 60 }, { 98, 89 }, { 45, 65 } };
		System.out.println("math: , physics:" + scores[0][0]);
		System.out.println("math: , physics:" + scores[0][1]);
		System.out.println("math: , physics:" + scores[1][0]);
		System.out.println("math: , physics:" + scores[1][1]);
		System.out.println("math: , physics:" + scores[2][0]);
		System.out.println("math: , physics:" + scores[2][1]);
		System.out.println("math: , physics:" + scores[3][0]);
		System.out.println("math: , physics:" + scores[3][1]);

	}

	public static void main(String[] args) {
		ajay();
	}

}
